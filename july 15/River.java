class River
{
	public static void water(String  place)
	{
		System.out.println(place);
	}
	public static void water(double totalRiver,String place)
	{
		System.out.println(totalRiver+place);
	}
	public static void water(String name1,String name2,String name3)
	{
		System.out.println(name1+name2+name3);
	}
	public static void water(boolean highestWater)
	{
		System.out.println(highestWater);
	}
	public static void water(double totalRiver,boolean highestWater)
	{
		System.out.println(totalRiver+highestWater);
	}
	public static void water(String place,double totalRiver)
	{
		System.out.println(place+totalRiver);
	}
	
}